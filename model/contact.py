class Contact:
    def __init__(self, first_name, last_name, adress, city, post_code, value_of_country,
                             zone_in_country):
        self.first_name = first_name
        self.last_name = last_name
        self.adress = adress
        self.city = city
        self.post_code = post_code
        self.value_of_country = value_of_country
        self.zone_in_country = zone_in_country